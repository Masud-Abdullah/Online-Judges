#include<bits/stdc++.h>
#define  nl         "\n"
#define  ll     long long
using namespace std;
int main()
{
    ll T,i;
    cin>>T;
    for(i=1;i<=T;i++)
    {
        ll a,b;
        cin>>a>>b;
        cout<<"Case "<<i<<": "<<a+b<<"\n";
    }

    return 0;
}


